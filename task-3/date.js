//hours to minutes
export function 
convertHoursToMinutes(hours){
    return hours * 60 ;
}

//minutes to hours
export function
convertMinutesToHours(minutes){
    return minutes /60 ;
}

//days to hours 
export function 
convertDaysToHours(days){
    return days * 24;
}

//hours to days
export function
convertHoursToDays(hours){
    return hours/24 ;
}

//minutes to seconds
export function
convertMinutesToSeconds(minutes){
    return minutes*60;
}

//seconds to minutes
export function
convertSecondsToMinutes(seconds){
    return seconds/60 ;
}
